/******************DRIVE IC寄存器寻址 8080 SERIES*******************************/
void ILI9163B_write_command(uint8 cmd)
{
	uint8 i;

	ILI9163_A0=0;	 						//A0=0发送命令
	ILI9163_CS=0;
	for(i=0;i<8;i++)
	{
		ILI9163_SCK=0;
		if((cmd&0x80)==0x80)	  	//高位先发送
			ILI9163_SDA=1;
		else		
			ILI9163_SDA=0;
		//ILI9163_SDA = (bit)(0x80&cmd);
		ILI9163_SCK = 1;
		cmd = (cmd<<1);

	}
    ILI9163_CS=1;
}

/*****************DRIVE IC寄存器写数据 8080 SERIES******************************/
void ILI9163B_write_para8(uint8 dat)
{  
	uint8 i;

	ILI9163_A0=1;			   				//A0=1发送数据
	ILI9163_CS=0;				
	for(i=0;i<8;i++)
	{
		ILI9163_SCK=0;
		if((dat&0x80)==0x80)
			ILI9163_SDA=1;
		else
			ILI9163_SDA=0;
	//	ILI9163_SDA = (bit)(0x80&dat);
		ILI9163_SCK = 1;
		dat = (dat<<1);

	}
	ILI9163_CS=1;
}
 
/****************DRIVE IC GDRAM 写数据 16BITS 8080 SERIES*****************************/
void ILI9163B_write_para16(uint16 dat)
{  		 
	uint8 i;
  uint8	buf;
	ILI9163_A0=1;
	ILI9163_CS=0;				

	buf = (uint8)(0xFF&(dat>>8));
	for(i=0;i<8;i++)
	{
		ILI9163_SCK=0;
		if((buf&0x80)==0x80)
			ILI9163_SDA=1;
		else
			ILI9163_SDA=0;
		//ILI9163_SDA = (bit)(0x80&buf);
		ILI9163_SCK=1;
		buf=(buf<<1);

	}
	ILI9163_CS=1;

	buf = (uint8)(0xFF&dat);
	ILI9163_CS=0;
	for(i=0;i<8;i++)
	{
		ILI9163_SCK=0;
		if((buf&0x80)==0x80)
			ILI9163_SDA=1;
		else
			ILI9163_SDA=0;
	//	ILI9163_SDA = (bit)(0x80&buf);
		ILI9163_SCK =1;
		buf=(buf<<1);
	}
	ILI9163_CS=1; 
}



